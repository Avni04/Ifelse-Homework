package ifelse;

public class ArithmeticOp {
    public static void main(String[] args) {
        int a= 20;
        int b= 25;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        int c= 30;
        System.out.println(c++);
        System.out.println(++c);
        System.out.println(c--);
        System.out.println(--c);


    }
}
